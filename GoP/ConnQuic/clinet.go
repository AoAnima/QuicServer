func (картаСессий КартаСессий) ОжиданиеОткрытияПотока() {
	for {
		// принимаем запрос на открытиые потока, и добавляем его в очередь потоков
		поток, err := картаСессий.Соединение.AcceptStream(context.Background())
		if err != nil {
			Ошибка("  %+v \n", err)
		}
		картаСессий.ОчередьПотоков.Добавить(поток)
	}
}

